# 50-ჯერ გამოქვეყნება for ციკლით
for i in range(50):
    print("GOA BEST!!!")

# 50-ჯერ გამოქვეყნება while ციკლით
i = 0
while i < 50:
    print("GOA BEST!!!")
    i += 1

# while loop-ის გამოყენებით, 1-დან 10-მდე რიცხვების დაბეჭდვა
i = 1
while i <= 10:
    print(i)
    i += 1

# while loop-ის გამოყენებით ლუწი რიცხვების დაბეჭდვა 1-დან 20-მდე
i = 2
while i <= 20:
    print(i)
    i += 2

# countdown 10-დან 1-მდე while loop-ით
i = 10
while i > 0:
    print(i)
    i -= 1

print("Blast off!")
